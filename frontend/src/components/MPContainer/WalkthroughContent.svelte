<p>
  Welcome to the Immersive Audio tool! The IA tool is an app to simplify the
  creation of immersive audio in the IAMF (.iamf) format. To begin, add a source
  audio element. Once at least one audio element has been created, a mix
  presentation can be created. A mix presentation groups several audio sources
  and specifies their mixed playback format. You can have multiple mix
  presentations in the same IAMF file. Audio elements may also be used between
  multiple mix presentations - that's the cool part of the IAMF format. With a
  mix presentation created, you can playback the mix presentation and tweak it
  until it's just right. When all mixes are ready, click the export button, name
  your file, and the IAMF encoder will deliver the final IAMF file.
</p>
